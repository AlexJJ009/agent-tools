def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    subparsers = parser.add_subparsers(dest="command")

    scan = subparsers.add_parser("scan", help="scan for Claude/Codex project context")
    scan.add_argument("--root", default=".", help="root directory to scan")
    scan.add_argument("--max-depth", type=int, default=3, help="max directory depth")
    scan.add_argument(
        "--include-config-only",
        action="store_true",
        help="include directories that only have .claude/.codex but no .git or entry file",
    )
    scan.set_defaults(func=run_scan)

    sync = subparsers.add_parser("sync", help="synchronize project context")
